package view;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.scene.image.Image;
import javafx.stage.Stage;

import java.net.URL;

public class ChangeUsernameViewGraphic extends Application {

    private static Stage stage;

    @Override
    public void start(Stage stage) throws Exception {
        ChangeUsernameViewGraphic.stage = stage;
        URL url = getClass().getResource("/ChangeUsername.fxml");
        Parent root = FXMLLoader.load(url);
        Scene scene = new Scene(root);
        stage.setScene(scene);
        stage.getIcons().add(new Image(String.valueOf(getClass().getResource("/icon.png"))));
        stage.setTitle("YU GI OH");
        stage.show();
    }

}
